//Find circumference of Triangle formula : a+b+c
#include<stdio.h>
int main()
{
	int a,b,c;
	printf("\n find the circumference of the triangle");
	printf("\n enter the value of a");
	scanf("\n %d",&a);
	printf("\n enter the value of b");
	scanf("\n %d",&b);
	printf("\n enter the value of c");
	scanf("\n %d",&c);
	
	printf("\n circumference of the triangle is %d",a+b+c);
	
	return 0;
	
}
