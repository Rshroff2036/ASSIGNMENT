//find the area of rectangle
//formula= length*width
#include<stdio.h>
int main()
{
	float length,width;
	printf("\n finding the area of rectangle");
	printf("\n enter the length of the rectangle");
	scanf("\n %f",&length);
	printf("\n enter the width of the rectangle");
	scanf("\n %f",&width);
	
	printf("\n area of the rectangle is %f",length*width);
	return 0;
}
